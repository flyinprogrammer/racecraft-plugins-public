class GeneratorError extends Error {
  constructor(category, message, sourcePath) {
    super(message);
    this.category = category;
    this.sourcePath = sourcePath;
  }
}
function toRepoPath(absolutePath) {
  const relative = path.relative(REPO_ROOT, absolutePath).split(path.sep).join('/');
  if (relative.startsWith('..') || path.isAbsolute(relative)) {
    throw new GeneratorError('source', `Path escapes repository root: ${absolutePath}`, relative);
  }
  return relative;
}
function absolute(repoPath) {
  const normalized = repoPath.split('/').join(path.sep);
  return path.join(REPO_ROOT, normalized);
}
function isGeneratedReferencePath(repoPath) {
  return /^docs-site\/src\/content\/docs\/reference\/(?:skills|agents|manifests|hooks|scripts|tests|source-vs-dist)\.md$/.test(repoPath);
}
function isAllowedSourcePath(repoPath) {
  if (!repoPath || repoPath.startsWith('../') || path.isAbsolute(repoPath)) return false;
  if (repoPath === '.git' || repoPath.startsWith('.git/')) return false;
  if (repoPath === '.worktrees' || repoPath.startsWith('.worktrees/')) return false;
  if (repoPath === 'node_modules' || repoPath.includes('/node_modules/')) return false;
  if (isGeneratedReferencePath(repoPath)) return false;
  if (ALLOWED_FILES.has(repoPath)) return true;
  return ALLOWED_PREFIXES.some((prefix) => repoPath.startsWith(prefix));
}
function assertAllowedSource(repoPath) {
  if (!isAllowedSourcePath(repoPath)) {
    throw new GeneratorError('source', `Source path is outside DOC-007 allowlist: ${repoPath}`, repoPath);
  }
}
async function exists(repoPath) {
  try {
    await fs.access(absolute(repoPath));
    return true;
  } catch {
    return false;
  }
}
async function readText(repoPath) {
  assertAllowedSource(repoPath);
  try {
    return await fs.readFile(absolute(repoPath), 'utf8');
  } catch (error) {
    throw new GeneratorError('source', `Unable to read source path: ${error.message}`, repoPath);
  }
}
async function readJson(repoPath) {
  const text = await readText(repoPath);
  try {
    return JSON.parse(text);
  } catch (error) {
    throw new GeneratorError('parse', `Invalid JSON: ${error.message}`, repoPath);
  }
}
async function listFiles(repoPath, predicate = () => true) {
  assertAllowedSource(repoPath.endsWith('/') ? repoPath : `${repoPath}/`);
  const root = absolute(repoPath);
  const files = [];
  async function walk(current) {
    let entries;
    try {
      entries = await fs.readdir(current, { withFileTypes: true });
    } catch (error) {
      throw new GeneratorError('source', `Unable to list source path: ${error.message}`, toRepoPath(current));
    }
    for (const entry of entries) {
      const abs = path.join(current, entry.name);
      const rel = toRepoPath(abs);
      if (entry.isDirectory()) {
        if (rel === 'node_modules' || rel.includes('/node_modules/') || rel === '.git' || rel.startsWith('.git/')) continue;
        await walk(abs);
      } else if (entry.isFile() && predicate(rel)) {
        assertAllowedSource(rel);
        files.push(rel);
      }
    }
  }
  if (await exists(repoPath)) {
    await walk(root);
  }
  return files.sort((a, b) => a.localeCompare(b));
}
function githubUrl(repoPath) {
  return `https://github.com/racecraft-lab/racecraft-plugins-public/blob/main/${encodeURI(repoPath)}`;
}
async function citation(repoPath, label = repoPath) {
  assertAllowedSource(repoPath);
  if (!(await exists(repoPath))) {
    throw new GeneratorError('source', `Citation path does not exist: ${repoPath}`, repoPath);
  }
  return { path: repoPath, label, githubUrl: githubUrl(repoPath) };
}
function sourceFact(text, sourceRefs) {
  return { text, sourceRefs };
}
function inferredNote(text, basedOn) {
  return { text, basedOn };
}
function firstSentence(text, fallback) {
  const cleaned = String(text || '').replace(/\s+/g, ' ').trim();
  if (!cleaned) return fallback;
  const match = cleaned.match(/^(.{20,220}?[.!?])(?:\s|$)/);
  if (match) return match[1];
  return cleaned.length > 220 ? `${cleaned.slice(0, 217).trimEnd()}...` : cleaned;
}
function titleFromSlug(slug) {
  return slug
    .split('-')
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}
function parseFrontmatter(text) {
  if (!text.startsWith('---\n')) return {};
  const end = text.indexOf('\n---', 4);
  if (end === -1) return {};
  const lines = text.slice(4, end).split('\n');
  const data = {};
  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const match = line.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!match) continue;
    const key = match[1];
    let value = match[2].trim();
    if (value === '>' || value === '|') {
      const parts = [];
      while (index + 1 < lines.length && /^\s+/.test(lines[index + 1])) {
        index += 1;
        parts.push(lines[index].trim());
      }
      value = parts.join(' ');
    }
    data[key] = value.replace(/^["']|["']$/g, '');
  }
  return data;
}
function parseTomlString(text, key) {
  const match = text.match(new RegExp(`^${key}\\s*=\\s*"([^"]*)"`, 'm'));
  return match ? match[1] : '';
}
async function parseSkill(repoPath) {
  const text = await readText(repoPath);
  const frontmatter = parseFrontmatter(text);
  const name = frontmatter.name || path.basename(path.dirname(repoPath));
  return {
    name,
    path: repoPath,
    description: frontmatter.description || firstSentence(text.replace(/^---[\s\S]*?---/, ''), `Skill ${name}`),
  };
}
async function parseClaudeAgent(repoPath) {
  const text = await readText(repoPath);
  const frontmatter = parseFrontmatter(text);
  const name = frontmatter.name || path.basename(repoPath, '.md');
  return {
    name,
    path: repoPath,
    description: frontmatter.description || firstSentence(text, `Claude Code agent ${name}`),
  };
}
async function parseCodexAgent(repoPath) {
  const text = await readText(repoPath);
  const name = parseTomlString(text, 'name') || path.basename(repoPath, '.toml');
  return {
    name,
    path: repoPath,
    description: parseTomlString(text, 'description') || `Codex custom agent ${name}`,
    model: parseTomlString(text, 'model'),
    effort: parseTomlString(text, 'model_reasoning_effort'),
  };
}
