function manifestRuntime(repoPath, json = {}) {
  if (repoPath === '.specify/integrations/speckit.manifest.json' || json.integration === 'speckit') return 'shared';
  if (json.integration === 'claude') return 'claude-code';
  if (repoPath.includes('codex') || repoPath.startsWith('.agents/')) return 'codex';
  if (repoPath.includes('claude') || repoPath.startsWith('.claude-plugin/')) return 'claude-code';
  return 'claude-code';
}
function runtimeLabel(runtime) {
  if (runtime === 'claude-code') return 'Claude Code';
  if (runtime === 'codex') return 'Codex';
  return 'shared Claude Code and Codex';
}
function manifestKind(repoPath) {
  if (repoPath.includes('marketplace.json')) return 'marketplace registry';
  if (repoPath.includes('.specify/integrations/')) return 'SpecKit integration manifest';
  if (repoPath.includes('dist/')) return 'generated distribution plugin manifest';
  if (repoPath.includes('.claude-plugin/plugin.json') || repoPath.includes('.codex-plugin/plugin.json')) return 'source plugin manifest';
  return 'manifest';
}
function manifestFieldSet(repoPath, json) {
  const runtime = manifestRuntime(repoPath, json);
  const topLevel = Object.keys(json).sort((a, b) => a.localeCompare(b));
  const requiredFields = runtime === 'codex'
    ? ['name', 'version', 'description']
    : ['name', 'description', 'version'];
  const optionalFields = topLevel.filter((field) => !requiredFields.includes(field));
  return {
    runtime,
    manifestKind: manifestKind(repoPath),
    requiredFields,
    optionalFields,
    sourceRefs: [repoPath],
  };
}
async function buildManifestsPage() {
  const paths = ['.claude-plugin/marketplace.json', '.agents/plugins/marketplace.json', 'speckit-pro/.claude-plugin/plugin.json', 'speckit-pro/.codex-plugin/plugin.json', '.specify/integrations/claude.manifest.json', '.specify/integrations/speckit.manifest.json', 'dist/claude/speckit-pro/.claude-plugin/plugin.json', 'dist/codex/speckit-pro/.codex-plugin/plugin.json'];
  const records = [];
  for (const repoPath of paths) {
    const json = await readJson(repoPath);
    const topLevel = Object.keys(json).sort((a, b) => a.localeCompare(b));
    const kind = manifestKind(repoPath);
    const runtime = manifestRuntime(repoPath, json);
    const source = await citation(repoPath);
    const facts = [
      sourceFact(`${repoPath} is categorized as ${kind}.`, [repoPath]),
      sourceFact(`Top-level fields: ${topLevel.map((field) => `\`${field}\``).join(', ')}.`, [repoPath]),
    ];
    if (json.version) facts.push(sourceFact(`The manifest declares version \`${json.version}\`.`, [repoPath]));
    if (json.plugins?.[0]?.source) facts.push(sourceFact(`The marketplace entry points to source \`${typeof json.plugins[0].source === 'string' ? json.plugins[0].source : JSON.stringify(json.plugins[0].source)}\`.`, [repoPath]));
    const fieldSets = repoPath.includes('plugin.json') ? [manifestFieldSet(repoPath, json)] : [];
    records.push({
      id: repoPath.replace(/[^a-zA-Z0-9]+/g, '-').replace(/^-|-$/g, '').toLowerCase(),
      heading: repoPath,
      purpose: `Reference inventory for the ${runtimeLabel(runtime)} ${kind}.`,
      platformMapping: {
        concept: kind,
        claudeCode: runtime !== 'codex' ? repoPath : 'Not a Claude Code manifest record.',
        codex: runtime !== 'claude-code' ? repoPath : 'Not a Codex manifest record.',
        runtimeDifference: 'Marketplace, plugin, integration, and generated distribution manifests are documented as separate categories.',
      },
      sourceFacts: facts,
      sources: [source],
      inferredNotes: [
        inferredNote('Required and optional field groupings are reference metadata for review; they do not change manifest semantics or generated payload content.', [repoPath]),
      ],
      classification: repoPath.startsWith('dist/') ? 'generated-payload' : 'source',
      manifestFieldSets: fieldSets,
    });
  }
  return page('manifests', 'Manifests Reference', 'Marketplace, plugin, integration, and generated distribution manifests with runtime field sets.', records, [
    await citation('speckit-pro/.claude-plugin/plugin.json'),
    await citation('speckit-pro/.codex-plugin/plugin.json'),
  ]);
}
