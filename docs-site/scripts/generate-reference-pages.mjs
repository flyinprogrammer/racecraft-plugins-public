async function buildSkillsPage() {
  const claudeSkills = new Map();
  for (const file of await listFiles('speckit-pro/skills', (rel) => rel.endsWith('/SKILL.md'))) {
    const skill = await parseSkill(file);
    claudeSkills.set(skill.name, skill);
  }
  const codexSkills = new Map();
  for (const file of await listFiles('speckit-pro/codex-skills', (rel) => rel.endsWith('/SKILL.md'))) {
    const skill = await parseSkill(file);
    codexSkills.set(skill.name, skill);
  }
  const names = Array.from(new Set([...claudeSkills.keys(), ...codexSkills.keys()])).sort((a, b) => a.localeCompare(b));
  const records = [];
  for (const name of names) {
    const claude = claudeSkills.get(name);
    const codex = codexSkills.get(name);
    const sourceRefs = [claude?.path, codex?.path].filter(Boolean);
    const sources = [];
    for (const source of sourceRefs) sources.push(await citation(source));
    const skillInferredNote = claude && codex
      ? 'Both runtime variants exist, so use the runtime-specific SKILL.md as the active instruction surface rather than treating the two files as interchangeable.'
      : `This skill is present only in the ${claude ? 'Claude Code' : 'Codex'} source tree; do not assume a parallel runtime surface exists unless a matching source file is added.`;
    records.push({
      id: name,
      heading: titleFromSlug(name),
      purpose: firstSentence(codex?.description || claude?.description, `SpecKit Pro skill ${name}`),
      platformMapping: {
        concept: `SpecKit Pro ${name} skill`,
        claudeCode: claude ? `/speckit-pro:${name}` : 'No Claude Code source skill in this repository.',
        codex: codex ? `$${name}` : 'No Codex source skill in this repository.',
        runtimeDifference: claude && codex ? 'Both runtimes ship source skill instructions; invocation syntax and runtime guardrails differ.' : 'This skill is present in only one runtime source tree.',
      },
      commandSkillReference: {
        claudeInvocation: claude ? `/speckit-pro:${name}` : 'Not available in the Claude Code source tree.',
        codexInvocation: codex ? `$${name}` : 'Not available in the Codex source tree.',
        purpose: firstSentence(codex?.description || claude?.description, `SpecKit Pro skill ${name}`),
        prerequisites: skillPrerequisites(name),
        expectedOutputArtifact: commandExpectedArtifact(name),
        sourceRefs,
      },
      sourceFacts: [
        sourceFact(`${name} has ${claude ? 'a Claude Code skill source' : 'no Claude Code skill source'} and ${codex ? 'a Codex skill source' : 'no Codex skill source'} in the checked-in plugin tree.`, sourceRefs),
      ],
      sources,
      inferredNotes: [
        inferredNote(skillInferredNote, sourceRefs),
      ],
      classification: 'source',
    });
  }
  return page('skills', 'Skills Reference', 'Claude Code and Codex skill surfaces, invocations, prerequisites, expected artifacts, and source citations.', records, [
    await citation('speckit-pro/skills/speckit-status/SKILL.md'),
    await citation('speckit-pro/codex-skills/speckit-status/SKILL.md'),
  ]);
}
