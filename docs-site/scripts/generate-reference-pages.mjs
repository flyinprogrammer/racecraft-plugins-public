function sourceVsDistRuntimeDifference(item) {
  if (item.classification === 'generated-payload') {
    return 'Generated payloads are runtime-specific install inventory; authoring changes belong in source paths and their owning build or release process.';
  }
  if (item.id === 'docs-site') {
    return 'Docs-site files are shared documentation infrastructure and are not runtime-specific install payloads.';
  }
  if (item.id === 'test-suite') {
    return 'Validation files are shared repository checks; runtime-specific assertions live inside the referenced test files.';
  }
  if (item.id === 'release-scripts') {
    return 'Release and payload scripts are shared maintenance infrastructure unless an individual script declares a runtime-specific target.';
  }
  if (item.id === 'speckit-integration-manifests') {
    return 'Integration manifests record installed SpecKit and Claude integration evidence; they are reference inventory, not generated plugin payloads.';
  }
  return 'Authoring-source and registry files are shared source responsibilities unless the referenced path names a runtime-specific surface.';
}
function page(slug, title, description, records, sources) {
  return {
    slug,
    title,
    description,
    publicPath: `/racecraft-plugins-public/reference/${slug}/`,
    outputPath: `${OUTPUT_DIR}/${slug}.md`,
    records: records.sort((a, b) => a.heading.localeCompare(b.heading)),
    generatedNotice: GENERATED_NOTICE,
    sources,
  };
}
function validateInventory(inventory) {
  if (!inventory || !Array.isArray(inventory.pages)) {
    throw new GeneratorError('internal', 'Inventory must contain a pages array.');
  }
  const slugs = inventory.pages.map((item) => item.slug);
  for (const slug of PAGE_SLUGS) {
    if (!slugs.includes(slug)) throw new GeneratorError('internal', `Missing generated page: ${slug}`);
  }
  if (slugs.length !== PAGE_SLUGS.length) throw new GeneratorError('internal', `Expected ${PAGE_SLUGS.length} pages, found ${slugs.length}.`);
  for (const generatedPage of inventory.pages) {
    if (!generatedPage.generatedNotice) throw new GeneratorError('internal', `Missing generated notice for ${generatedPage.slug}.`);
    if (!generatedPage.outputPath.endsWith(`${generatedPage.slug}.md`)) throw new GeneratorError('internal', `Output path mismatch for ${generatedPage.slug}.`);
    if (!generatedPage.sources?.length) throw new GeneratorError('internal', `Missing page-level sources for ${generatedPage.slug}.`);
    for (const source of generatedPage.sources) validateCitation(source);
    for (const record of generatedPage.records) {
      if (!record.id || !record.heading || !record.purpose) throw new GeneratorError('internal', `Incomplete record on ${generatedPage.slug}.`);
      if (!record.sources?.length) throw new GeneratorError('internal', `Record ${record.id} on ${generatedPage.slug} has no sources.`);
      for (const source of record.sources) validateCitation(source);
      for (const fact of record.sourceFacts || []) {
        if (!fact.text || !fact.sourceRefs?.length) throw new GeneratorError('internal', `Record ${record.id} has an invalid source fact.`);
        fact.sourceRefs.forEach(assertAllowedSource);
      }
      for (const note of record.inferredNotes || []) {
        if (!note.text || !note.basedOn?.length) throw new GeneratorError('internal', `Record ${record.id} has an invalid inferred note.`);
        note.basedOn.forEach(assertAllowedSource);
      }
      if (record.commandSkillReference) {
        for (const field of ['claudeInvocation', 'codexInvocation', 'purpose', 'expectedOutputArtifact']) {
          if (!record.commandSkillReference[field]) throw new GeneratorError('internal', `Command/skill record ${record.id} is missing ${field}.`);
        }
      }
      if (record.manifestFieldSets) {
        for (const fieldSet of record.manifestFieldSets) {
          if (!fieldSet.runtime || !fieldSet.manifestKind || !fieldSet.sourceRefs?.length) {
            throw new GeneratorError('internal', `Manifest record ${record.id} has an invalid field set.`);
          }
        }
      }
    }
  }
}
function validateCitation(source) {
  if (!source.path || !source.githubUrl) throw new GeneratorError('internal', 'Invalid citation object.');
  assertAllowedSource(source.path);
}
function escapeMarkdown(text) {
  return String(text || '').replace(/[\\|[\]]/g, (character) => `\\${character}`);
}
function renderSources(sources) {
  return sources
    .map((source) => `- [${escapeMarkdown(source.label || source.path)}](${source.githubUrl})`)
    .join('\n');
}
function renderList(values) {
  if (!values?.length) return '- Not applicable.';
  return values.map((value) => `- ${value}`).join('\n');
}
function renderPage(generatedPage) {
  const lines = [
    '---',
    `title: "${generatedPage.title}"`,
    '---',
    '',
    generatedPage.description,
    '',
    `> ${generatedPage.generatedNotice}`,
    '',
    '## Page Summary',
    '',
    `- **Public path:** \`${generatedPage.publicPath}\``,
    `- **Generated output:** \`${generatedPage.outputPath}\``,
    `- **Records:** ${generatedPage.records.length}`,
    '',
    '## Navigation Summary',
    '',
    ...generatedPage.records.map((record) => `- ${escapeMarkdown(record.heading)}`),
    '',
    '## Page Sources',
    '',
    renderSources(generatedPage.sources),
    '',
    '## Records',
    '',
  ];
  for (const record of generatedPage.records) {
    lines.push(`### ${record.heading}`, '');
    lines.push(`- **Purpose:** ${record.purpose}`);
    if (record.classification) lines.push(`- **Classification:** \`${record.classification}\``);
    if (record.platformMapping) {
      lines.push(`- **Platform concept:** ${record.platformMapping.concept || 'Not applicable.'}`);
      lines.push(`- **Claude Code:** ${record.platformMapping.claudeCode || 'Not applicable.'}`);
      lines.push(`- **Codex:** ${record.platformMapping.codex || 'Not applicable.'}`);
      lines.push(`- **Runtime difference:** ${record.platformMapping.runtimeDifference || 'Not applicable.'}`);
    }
    lines.push('');
    if (record.commandSkillReference) {
      lines.push('#### Command Or Skill Reference', '');
      lines.push(`- **Claude Code invocation:** \`${record.commandSkillReference.claudeInvocation}\``);
      lines.push(`- **Codex invocation:** \`${record.commandSkillReference.codexInvocation}\``);
      lines.push(`- **Purpose:** ${record.commandSkillReference.purpose}`);
      lines.push(`- **Expected output artifact:** ${record.commandSkillReference.expectedOutputArtifact}`);
      lines.push('');
      lines.push('**Prerequisites**');
      lines.push('');
      lines.push(renderList(record.commandSkillReference.prerequisites));
      lines.push('');
    }
    if (record.manifestFieldSets?.length) {
      lines.push('#### Manifest Field Sets', '');
      for (const fieldSet of record.manifestFieldSets) {
        lines.push(`- **${fieldSet.runtime} ${fieldSet.manifestKind}:**`);
        lines.push(`  - Required fields: ${fieldSet.requiredFields.map((field) => `\`${field}\``).join(', ') || 'None recorded.'}`);
        lines.push(`  - Optional fields: ${fieldSet.optionalFields.map((field) => `\`${field}\``).join(', ') || 'None recorded.'}`);
      }
      lines.push('');
    }
    lines.push('#### Source Facts', '');
    lines.push(renderList((record.sourceFacts || []).map((fact) => `${fact.text} Source refs: ${fact.sourceRefs.map((ref) => `\`${ref}\``).join(', ')}.`)));
    lines.push('');
    lines.push('#### Sources', '');
    lines.push(renderSources(record.sources));
    lines.push('');
    lines.push('#### Inferred Notes', '');
    if (record.inferredNotes?.length) {
      for (const note of record.inferredNotes) {
        lines.push(`- ${note.text}`);
        lines.push(`  - Based on: ${note.basedOn.map((ref) => `\`${ref}\``).join(', ')}`);
      }
    } else {
      lines.push('- None.');
    }
    lines.push('');
  }
  return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trimEnd()}\n`;
}
async function buildInventory() {
  const pages = [
    await buildSkillsPage(),
    await buildAgentsPage(),
    await buildManifestsPage(),
    await buildHooksPage(),
    await buildScriptsPage(),
    await buildTestsPage(),
    await buildSourceVsDistPage(),
  ];
  const inventory = { pages };
  validateInventory(inventory);
  return inventory;
}
async function writeOutput(renderedPages) {
  await fs.mkdir(absolute(OUTPUT_DIR), { recursive: true });
  for (const renderedPage of renderedPages) {
    try {
      await fs.writeFile(absolute(renderedPage.outputPath), renderedPage.content, 'utf8');
    } catch (error) {
      throw new GeneratorError('output-write', `Unable to write generated output: ${error.message}`, renderedPage.outputPath);
    }
  }
  process.stdout.write(`Generated ${renderedPages.length} reference pages.\n`);
}
async function checkOutput(renderedPages) {
  const stale = [];
  for (const renderedPage of renderedPages) {
    let existing = '';
    try {
      existing = await fs.readFile(absolute(renderedPage.outputPath), 'utf8');
    } catch {
      stale.push(renderedPage.outputPath);
      continue;
    }
    if (existing !== renderedPage.content) stale.push(renderedPage.outputPath);
  }
  if (stale.length) {
    for (const outputPath of stale) process.stdout.write(`stale: ${outputPath}\n`);
    process.stdout.write('Run: pnpm --dir docs-site reference:generate\n');
    process.exitCode = 1;
  } else {
    process.stdout.write('Reference pages are current.\n');
  }
}
async function main() {
  const inventory = await buildInventory();
  const renderedPages = inventory.pages.map((generatedPage) => ({
    outputPath: generatedPage.outputPath,
    content: renderPage(generatedPage),
  }));
  if (CHECK_MODE) {
    await checkOutput(renderedPages);
  } else {
    await writeOutput(renderedPages);
  }
}
main().catch((error) => {
  if (error instanceof GeneratorError) {
    process.stderr.write(`${error.category}: ${error.sourcePath ? `${error.sourcePath}: ` : ''}${error.message}\n`);
  } else {
    process.stderr.write(`internal: ${error.stack || error.message}\n`);
  }
  process.exitCode = 2;
});
