commit_fixture_change() {
  local repo="$1" msg="$2"
  git -C "$repo" add -A
  git -C "$repo" commit -qm "$msg"
}

run_relocator() {
  local out_var="$1" err_var="$2"
  shift 2
  local stdout_file="$FIXTURE_DIR/stdout.$RANDOM"
  local stderr_file="$FIXTURE_DIR/stderr.$RANDOM"
  local rc=0
  "$@" >"$stdout_file" 2>"$stderr_file" || rc=$?
  printf -v "$out_var" '%s' "$(cat "$stdout_file")"
  printf -v "$err_var" '%s' "$(cat "$stderr_file")"
  return "$rc"
}

