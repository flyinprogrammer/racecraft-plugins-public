json_check() {
  local json="$1" expr="$2" msg="$3"
  local file="$FIXTURE_DIR/json-check.json"
  printf '%s' "$json" > "$file"
  if python3 - "$file" "$expr" >/dev/null 2>&1 <<'PY'
import json
import sys

with open(sys.argv[1], encoding="utf-8") as fh:
    data = json.load(fh)

expr = sys.argv[2]
safe_builtins = {"any": any, "all": all, "len": len, "list": list, "sorted": sorted}
if not eval(expr, {"__builtins__": safe_builtins}, {"data": data}):
    raise SystemExit(1)
PY
  then
    _pass
  else
    _fail "$msg"
  fi
}

assert_compact_json_object() {
  local json="$1"
  if printf '%s' "$json" | python3 -c 'import json, sys; data=json.load(sys.stdin); sys.exit(0 if isinstance(data, dict) else 1)' >/dev/null 2>&1 \
    && [[ "$json" != *$'\n'* ]]; then
    _pass
  else
    _fail "expected one compact JSON object on stdout"
  fi
}

assert_dir_exists() {
  local path="$1" msg="${2:-}"
  if [ -d "$path" ]; then
    _pass
  else
    _fail "${msg:+$msg: }directory not found: $path"
  fi
}

