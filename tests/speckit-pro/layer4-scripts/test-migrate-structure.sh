json_field() {
  local json="$1" field="$2"
  printf '%s' "$json" | python3 -c "
import json, sys
try:
    data = json.load(sys.stdin)
    value = data
    for part in '$field'.split('.'):
        value = value[part]
    print(value)
except Exception:
    print('__parse_error__')
" 2>/dev/null || printf '__parse_error__'
}

json_has_item() {
  local json="$1" action="$2" path="$3" reason="${4:-}"
  printf '%s' "$json" | python3 -c "
import json, sys
try:
    data = json.load(sys.stdin)
except Exception:
    sys.exit(1)
for item in data.get('items', []):
    if item.get('action') != '$action':
        continue
    pathish = item.get('path', item.get('target', item.get('id', '')))
    if pathish != '$path':
        continue
    if '$reason' and item.get('reason') != '$reason':
        continue
    sys.exit(0)
sys.exit(1)
" 2>/dev/null
}

json_check() {
  local json="$1" expr="$2" msg="$3"
  local file="$WORK/json-check.$RANDOM.json"
  printf '%s' "$json" > "$file"
  if python3 - "$file" "$expr" >/dev/null 2>&1 <<'PY'
import json
import sys

with open(sys.argv[1], encoding="utf-8") as fh:
    data = json.load(fh)

expr = sys.argv[2]
safe_builtins = {"any": any, "all": all, "len": len, "list": list, "sorted": sorted, "str": str}
if not eval(expr, {"__builtins__": safe_builtins}, {"data": data}):
    raise SystemExit(1)
PY
  then
    _pass
  else
    _fail "$msg"
  fi
}

assert_dir_exists() {
  local path="$1" msg="${2:-}"
  if [ -d "$path" ]; then
    _pass
  else
    _fail "${msg:+$msg: }directory not found: $path"
  fi
}

assert_dir_not_exists() {
  local path="$1" msg="${2:-}"
  if [ ! -d "$path" ]; then
    _pass
  else
    _fail "${msg:+$msg: }directory should not exist: $path"
  fi
}

run_migrate() {
