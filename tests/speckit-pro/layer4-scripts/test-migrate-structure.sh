make_invalid_active_fixture() {
  local repo="$1" variant="$2"
  make_repo_fixture "$repo"

  case "$variant" in
    malformed_json)
      printf '{"feature_directory":' > "$repo/.specify/feature.json"
      ;;
    missing_key)
      printf '{"other":"specs/prsg-013a-active"}\n' > "$repo/.specify/feature.json"
      ;;
    non_string)
      printf '{"feature_directory":42}\n' > "$repo/.specify/feature.json"
      ;;
    empty_string)
      printf '{"feature_directory":""}\n' > "$repo/.specify/feature.json"
      ;;
    outside_repo)
      printf '{"feature_directory":"../outside-repo"}\n' > "$repo/.specify/feature.json"
      ;;
    *)
      printf 'unknown invalid active-feature variant: %s\n' "$variant" >&2
      return 2
      ;;
  esac

  git -C "$repo" add .specify/feature.json
  git -C "$repo" commit -qm "seed invalid active-feature fixture"
}

snapshot_files() {
  local root="$1"
  (cd "$root" && find . -type f ! -path './.git/*' -print0 | LC_ALL=C sort -z | xargs -0 shasum)
}

